#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	len;

	len = 0;
	while (s[len])
		len++;
	while (s[len] != (char)c)
	{
		if (len == 0)
			return (NULL);
		len--;
	}
	return ((char*)&s[len]);
}
