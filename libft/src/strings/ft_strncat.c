#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t	i;
	int		len1;

	i = 0;
	len1 = 0;
	while (s1[len1])
		len1++;
	while (s2[i] != '\0' && n != i)
	{
		s1[len1 + i] = s2[i];
		i++;
	}
	s1[len1 + i] = '\0';
	return (s1);
}
