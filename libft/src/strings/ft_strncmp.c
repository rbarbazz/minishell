#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t			i;
	unsigned char	*t1;
	unsigned char	*t2;

	i = 0;
	t2 = (unsigned char*)s2;
	t1 = (unsigned char*)s1;
	while ((t1[i] != '\0' || t2[i] != '\0') && n > i)
	{
		if (t1[i] != t2[i])
			return (t1[i] - t2[i]);
		i++;
	}
	return (0);
}
